# 变量
"""
格式为
变量名 = 变量值

变量 : 在程序运行中存储数据
特征 : 存贮的值可以发生改变
print 中可以斜土多分内容 , 可通过 ',' 进行分割

"""

money = 6000

print("钱包里还有", money, "元")

money -= 10
print("钱包里还有", money, "元")


# 假设一个定时小程序 __ 每隔一小时显示钱包余额 , 现在是下午1点钱包剩余
print("钱包里还有", money, "元")
money -= 10
print("钱包里还有", money, "元")
money -= 10
print("钱包里还有", money, "元")
money -= 10
print("钱包里还有", money, "元")
money -= 10