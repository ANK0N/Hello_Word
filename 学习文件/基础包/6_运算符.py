# 数学运算符
"""
+  加
-  减
*  乘
/  除
// 取整除
%  取余
** 指数 (a**b 指a的b次方)
"""
print("1+1=", 1 + 1)
print("1-1=", 1 - 1)
print("11/2=", 11 / 2)
print("11//2=", 11 // 2)
print("11%2", 11 % 2)
print("11**2", 11 ** 2)

# 赋值运算符  =
"""
会将变量操作过后重新赋值给变量
+= 
-=
*=
/=
//=
%=
**=
"""
num = 1
num += 1
print('num += 1', num)
num -= 1
print('num -= 1', num)
num *= 8
print('num *= 4', num)
num /= 2
print('num /= 2', num)
num %= 2
print('num %= 2', num)
num //= 3
print('num //= 3', num)

